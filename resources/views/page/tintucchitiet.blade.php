           <div class="clearfix"></div>
           <div class="brief"><p><span style="font-size:16px;"><span style="font-family:Arial, Helvetica, sans-serif;"><strong>Sinh tố lá hẹ và củ sen có tác dụng vừa bồi bổ cơ thể, an thần, chống suy nhược, vừa được coi là một trong những bài thuốc phòng chống ung thư hữu hiệu.</strong></span></span></p>
           </div>
           <p>Chị Bùi Thu Thủy - một người đã mắc bệnh ung thư - chia sẻ cách làm sinh tố củ sen và sinh tố lá hẹ. Hàng ngày, chị đều sử dụng 2 loại thức uống này vào buổi sáng và buổi chiều trong thực đơn dinh dưỡng của mình.</p>
           <p>Hai loại sinh tố củ sen và sinh tố lá hẹ có công dụng rất tuyệt vời, đặc biệt là với bệnh nhân ung thư.</p>
           <h5>Sinh tố lá hẹ</h5>
           <p>Lá hẹ có công dụng rất tuyệt vời. Nó chứa chất flavonoid và lưu huỳnh tự nhiên, có tác dụng chống lại một số bệnh ung thư như ung thư da, ung thư dạ dày và ung thư vú.</p>
           <div class="text-center"><a href=""><img src="https://biggreen.vn/upload/20366/fck/files/sinh%20t%E1%BB%91%20l%C3%A1%20h%E1%BA%B9.jpg"></a></div>
           <p class="text-center">Nguyên liệu: Lá hẹ, táo đỏ hoặc táo xanh, sữa chua.</p>
           <p>Cách làm:</p>
           	<p>- Cắt lá hẹ thành từng đoạn nhỏ khoảng 5cm. </p>
           	<p>- Táo để nguyên vỏ, cắt nhỏ thành từng miếng vuông khoảng 2cm. Tùy vào khẩu vị của mỗi người. Táo chứa nhiều vitamin C và các chất chống oxy hóa.</p>
           <div class="text-center">
              <a href=""><img src="https://biggreen.vn/upload/20366/fck/files/sinh%20t%E1%BB%91%20l%C3%A1%20h%E1%BA%B92.jpg"></a>
           </div>
           <p class="text-center">Tỷ lệ lá hẹ và táo tùy vào khẩu vị của mỗi người. Thông thường, bạn có thể lấy 50g lá hẹ và 1 trái táo.</p>
           <p>- Cho táo, lá hẹ, sữa chua vào máy xay sinh tố. Như vậy, bạn đã có một cốc sinh tố lá hẹ thơm ngon, bổ dưỡng</p>
           <div class="text-center">
              <a href=""><img src="https://biggreen.vn/upload/20366/fck/files/sinh%20t%E1%BB%91%20l%C3%A1%20h%E1%BA%B93.jpg"></a>
           </div>
           <p class="text-center">Táo và sữa chua sẽ át đi mùi hăng của lá hẹ khiến cốc sinh tố vô cùng thơm ngon.</p>
           <h5>Sinh tố củ sen</h5>
           <p>Củ sen tươi chứa nhiều khoáng chất như canxi, sắt, phốt pho, vitamin B, C… Củ sen cũng chứa nhiều nguyên tố vi lượng có tác dụng bổ sung khí huyết.</p>
           <div class="text-center">
            <a href=""><img src="https://biggreen.vn/upload/20366/fck/files/sinh%20t%E1%BB%91%20c%E1%BB%A7%20sen.jpg"></a>
           </div>
           <p class="text-center">Nguyên liệu gồm củ sen tươi và sữa chua</p>
           <p>Cách làm: Củ sen sau khi gọt vỏ, cắt thành những miếng tròn dày khoảng 1cm. Sau đó cho vào máy xay sinh tố cùng sữa chua.Loại thức uống này có vị thơm ngon của củ sen tươi mà không bị quá ngọt.</p>
